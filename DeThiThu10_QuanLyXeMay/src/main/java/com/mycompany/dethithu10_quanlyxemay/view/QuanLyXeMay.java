    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            index = tblXeMay.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 dòng để xóa");
                return;
            }
            String ma = (String) model.getValueAt(index, 0);
            xms.DeleteXM(ma);
            JOptionPane.showMessageDialog(this, "Xóa Xe Máy thành công");
            xms.loatDataTiTable(model);
            clearForm();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

