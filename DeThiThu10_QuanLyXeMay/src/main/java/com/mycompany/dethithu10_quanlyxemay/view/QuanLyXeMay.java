    private String formatCurrency(double value) {
        DecimalFormat decimalFormat = new DecimalFormat("#,###.##");
        return decimalFormat.format(value);
    }

    private void showDetail(int i) {
        if (i >= 0 && i < model.getRowCount()) {
            txtMaXM.setText((String) model.getValueAt(i, 0));
            txtTen.setText((String) model.getValueAt(i, 1));
            double giaBan = Double.parseDouble(model.getValueAt(i, 2).toString());
            String formattedGiaBan = formatCurrency(giaBan);
            txtGiaBan.setText(formattedGiaBan);
            txtSoLuong.setText(model.getValueAt(i, 3) + "");
            cboTrangThai.setSelectedItem(model.getValueAt(i, 4));
            tblXeMay.setRowSelectionInterval(i, i);
        }
    }

    private void clearForm() {
        txtMaXM.setText("");
        txtTen.setText("");
        txtSoLuong.setText("");
        txtGiaBan.setText("");
    }

    private boolean validateForm() {
        if (txtMaXM.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ mã số xe máy");
            txtMaXM.requestFocus();
            return false;
        }

        if (txtTen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ tên xe máy");
            txtTen.requestFocus();
            return false;
        }

        double giaBan = Double.parseDouble(txtGiaBan.getText());
        if (txtGiaBan.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ giá bán xe máy");
            txtGiaBan.requestFocus();
            return false;
        }

        int soLuong = Integer.parseInt(txtSoLuong.getText());
        if (txtSoLuong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ số lượng xe máy");
            txtSoLuong.requestFocus();
            return false;
        }
        return true;
    }

    private XeMay readForm() {
        XeMay xm = new XeMay();
        String ma = txtMaXM.getText();
        String ten = txtTen.getText();
        double giaBan = Double.parseDouble(txtGiaBan.getText());
        int soLuong = Integer.parseInt(txtSoLuong.getText());
        int trangThai;
        if (cboTrangThai.getSelectedItem().equals("Xe cũ")) {
            trangThai = 1;
        } else {
            trangThai = 0;
        }
        return new XeMay(ma, ten, giaBan, soLuong, trangThai);
    }
